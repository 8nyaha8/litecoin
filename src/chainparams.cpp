        assert(genesis.hashMerkleRoot == uint256S("0xbc01ce873e0100fdfb30a5e817296f8b0d6a2b9f0da3cd8e47ed47b0dc0313ae"));

        // Note that of those with the service bits flag, most only support a subset of possible options

